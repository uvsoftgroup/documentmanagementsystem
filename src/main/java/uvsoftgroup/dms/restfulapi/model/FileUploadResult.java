package uvsoftgroup.dms.restfulapi.model;

public class FileUploadResult {
	    private Long furId;
		private String furName;
		private String furType;
		private Long furSize;
		public Long getFurId() {
			return furId;
		}
		public void setFurId(Long furId) {
			this.furId = furId;
		}
		public String getFurName() {
			return furName;
		}
		public void setFurName(String furName) {
			this.furName = furName;
		}
		public String getFurType() {
			return furType;
		}
		public void setFurType(String furType) {
			this.furType = furType;
		}
		public Long getFurSize() {
			return furSize;
		}
		public void setFurSize(Long furSize) {
			this.furSize = furSize;
		}
		
		
}
